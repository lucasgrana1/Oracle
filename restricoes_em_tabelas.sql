--# TIPOS DE RESTRIÇOES
NOT NULL = Especifica que uma coluna nao pode conter valores nulos
UNIQUE = Designa uma coluna ou uma combinaçao de colunas como exclusiva
PRIMARY KEY = Designa uma colouna ou uma cobinaçao de colunas como a chave primaria da tabela
FOREING KEY = designa uma coluna ou uma combinaçao de colunas como a chave estrangeira em uma restriçao de integridade refencial
CHECK = Especifica uma condiçao que deve ser atendida por cada linha da tabela
