--# VER TAMANHO DA BASE TODA
----------------------------
SET LINES 237 PAGES 10000 LONG 100000 FEED 1 ECHO ON TI ON TRIM ON TERM ON SERVEROUT ON TAB OFF VER OFF DESC LINENUM ON
COL SIZE_MB  FOR 999,990  HEA "TAMANHO_MB"
COL SIZE_GB  FOR 999,990  HEA "TAMANHO_GB"
select ceil(sum(s.bytes)/1024/1024) as SIZE_MB, ceil(sum(s.bytes)/1024/1024/1024) as SIZE_GB from dba_segments S where s.segment_type NOT in ('ROLLBACK','TYPE2 UNDO');
